public class Car {
    int carId;
    String brand;
    String model;
    void setCarId(int cid){
        carId=cid;
    }
    void setBrand(String sbrand){
        brand=sbrand;
    }
    void setModel(String smodel){
        model=smodel;
    }
    int getCarId(){
        return carId;
    }
    String getBrand(){
        return brand;
    }
    String getModel(){
        return model;
    } 
}
